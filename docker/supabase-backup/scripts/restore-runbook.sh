## 8. Verification checklist

- [ ] `restore-logical` printed a non-zero user-table count for `@@DB1@@`.
- [ ] `auth.users` row count looks right (`select count(*) from auth.users;`).
- [ ] Supabase Studio loads and login works (secrets §5 correct).
- [ ] A row with an encrypted/Vault column decrypts (pgsodium key §6 correct).
- [ ] A previously-uploaded file downloads from Storage (§7 correct).
- [ ] `select pg_is_in_recovery();` returns `f` (PITR promoted) — Path B only.

If every box is checked, the restore is complete.

---

## 9. Verifying backups (without a full restore)

The scheduled `verify` job runs a **structural** check by default — it confirms
each object exists, is non-trivially sized, and (for age) starts with a valid age
header. It does **not** decrypt, because the backup box only holds the age
**public** key. That is by design: a compromised backup host cannot read your data.

To validate a backup **end-to-end** (decrypt + `pg_restore --list`) periodically,
run `verify` in a one-off container **with the private key mounted** — on a trusted
machine, never by leaving the key on the always-on backup service:

```bash
docker run --rm \
  -e PGPASSWORD=unused \
  -e BACKUP_S3_ENDPOINT -e BACKUP_S3_BUCKET -e BACKUP_S3_REGION \
  -e BACKUP_S3_FORCE_PATH_STYLE -e BACKUP_PREFIX \
  -e BACKUP_S3_ACCESS_KEY -e BACKUP_S3_SECRET_KEY \
  -e BACKUP_ENCRYPTION -e BACKUP_AGE_IDENTITY_FILE=/keys/age.key \
  -e VERIFY_DECRYPT=true \
  -v "${AGE_KEY_HOST_PATH}:/keys/age.key:ro" \
  @@IMAGE@@ verify
```

Add `-e VERIFY_FULL=true -e VERIFY_PGHOST=<scratch-db-host>` to also test-restore
into a throwaway database and count tables.

