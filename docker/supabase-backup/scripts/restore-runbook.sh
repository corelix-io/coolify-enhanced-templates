## 7. Re-hydrate Storage objects into MinIO

The database only holds object *metadata*; the bytes live in MinIO. Restore them
from the latest `storage/` snapshot (~@@STORAGE_OBJECTS@@ objects):

```bash
# 1) download + decrypt the storage archive to a local dir
docker run --rm -it \
  -e PGPASSWORD=unused \
  -e BACKUP_S3_ENDPOINT -e BACKUP_S3_BUCKET -e BACKUP_S3_REGION \
  -e BACKUP_S3_FORCE_PATH_STYLE -e BACKUP_PREFIX \
  -e BACKUP_S3_ACCESS_KEY -e BACKUP_S3_SECRET_KEY \
  -e BACKUP_ENCRYPTION -e BACKUP_AGE_IDENTITY_FILE=/keys/age.key \
  -v "${AGE_KEY_HOST_PATH}:/keys/age.key:ro" \
  -v "$PWD/storage-restore:/out" \
  @@IMAGE@@ bash -lc \
    'source /scripts/lib.sh; s3_get_stream "storage/@@SNAP_STORAGE@@/storage.tar.gz@@EXT@@" | decrypt_stream | tar -C /out -xzf -'

# 2) push the objects back into the NEW MinIO with mc or rclone, e.g.:
#    rclone sync ./storage-restore  minio:<STORAGE_BUCKET>
```

Point `MINIO_*` at the new stack's MinIO. Verify object counts match (~@@STORAGE_OBJECTS@@).

